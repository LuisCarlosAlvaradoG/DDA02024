# Ejercicio 1: Cambio de Letreros (Ejemplo proporcionado)
# Problema: Tenemos tres costalitos de dulces; uno contiene sólo dulces sabor chocolate; 
# otro contiene dulces de fresa; el tercero contiene una mezcla de dulces de los dos sabores. 
# Se supone que cada costalito debe tener un letrero que indica el sabor de los dulces que contiene. 
# El problema surge cuando un ocioso cambió la posición de los letreros, 
# asegurándose de que ningún letrero correspondiera con el contenido del costal. 
# Lo que hay que hacer es determinar el menor número de dulces que debemos extraer de los costales, 
# e indicar de qué costales hay que extraerlos, para poder saber en dónde debe ir cada letrero.

# Ejercicio 2: El Puzzle de los Prisioneros y el Carcelero
# Problema: Un carcelero les propone un reto a tres prisioneros. Les pone un sombrero a cada uno, que puede ser de color rojo o azul. 
# Cada prisionero puede ver los sombreros de los otros dos, pero no el suyo propio. 
# El carcelero les dice que, si al menos uno de ellos puede adivinar el color de su propio sombrero, todos serán liberados. 
# De lo contrario, todos serán ejecutados. ¿Cómo pueden los prisioneros coordinarse para asegurar su libertad?

# Ejercicio 3: Los Tres Interruptores y las Tres Bombillas
# Problema: Tienes tres interruptores en una habitación y tres bombillas en otra. Cada interruptor controla una bombilla, pero no sabes cuál. 
# Puedes accionar los interruptores como desees y luego ir a la otra habitación para ver las bombillas una sola vez. 
# ¿Cómo puedes determinar qué interruptor controla cada bombilla?

# Ejercicio 4: Cruzar el Río con el Lobo, la Cabra y la Col
# Problema: Un granjero debe cruzar un río con un lobo, una cabra y una col. 
# Tiene un bote que sólo puede llevar dos cosas (incluido él mismo) a la vez. Si deja al lobo solo con la cabra, el lobo se comerá a la cabra.
#  Si deja a la cabra sola con la col, la cabra se comerá la col. ¿Cómo puede el granjero cruzar el río con todos los elementos intactos?

# Ejercicio 5: Los Misioneros y los Caníbales
# Problema: Tres misioneros y tres caníbales deben cruzar un río utilizando un bote que puede llevar como máximo a dos personas. 
# En cualquier momento, si los caníbales superan en número a los misioneros en cualquiera de las dos orillas, 
# los caníbales se comerán a los misioneros. ¿Cómo pueden cruzar todos de manera segura?

# Ejercicio 6: El Problema de las Torres de Hanoi
# Problema: Tienes tres varillas y una serie de discos de diferentes tamaños que pueden deslizarse sobre cualquier varilla. 
# Los discos están apilados en orden creciente de tamaño en una de las varillas. El objetivo es mover toda la pila a otra varilla, 
# obedeciendo las siguientes reglas: solo se puede mover un disco a la vez, 
# un disco más grande no puede ser colocado sobre un disco más pequeño, 
# y cada movimiento consiste en tomar el disco superior de una de las pilas y colocarlo en otra varilla.

# Ejercicio 7: El Problema de las Botellas de Agua
# Problema: Tienes una botella de 5 litros y una de 3 litros. Ambas botellas no tienen marcas de medida y puedes llenarlas de agua,
#  vaciarlas o verter el contenido de una en la otra. 
# ¿Cómo puedes medir exactamente 4 litros usando solo estas dos botellas?

# Ejercicio 8: El Problema de las Pesas
# Problema: Tienes 12 bolas aparentemente idénticas, pero una de ellas es de diferente peso. 
# Usando una balanza de dos platos y solo tres pesadas, ¿cómo puedes determinar cuál es la bola diferente y 
# si es más ligera o más pesada que las demás?

# Ejercicio 9: La Habitación de las Monedas
# Problema: Hay una habitación con 100 monedas en el suelo, 10 de ellas mostrando cara y 90 mostrando cruz. 
# No puedes ver, sentir, o de alguna manera determinar cuál es cuál. 
# ¿Cómo puedes dividir las monedas en dos grupos de manera que cada grupo contenga el mismo número de monedas mostrando cara?

# Ejercicio 10: El Problema de los Cinco Piratas
# Problema: Cinco piratas deben dividir un tesoro de 100 monedas de oro. Siguen estas reglas:

# El pirata más anciano propone cómo dividir las monedas.
# Todos los piratas, incluyendo el que propone, votan a favor o en contra de la propuesta.
# Si la mayoría acepta la propuesta, las monedas se dividen según lo propuesto.
# Si la propuesta es rechazada, el pirata que la propuso es arrojado al mar y el proceso se repite con los piratas restantes.
# Los piratas son extremadamente inteligentes, y saben que, en caso de empate en la votación, la propuesta es aceptada. 
# ¿Cómo debería el pirata más anciano dividir las monedas para asegurarse de obtener la mayor cantidad posible?